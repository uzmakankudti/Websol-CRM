-- =====================================================================
-- Migration 007 — Dispatch & Delivery Management
--
-- Implements: dispatch orders that carry one or more allocated printers
-- to a customer site, with a four-stage lifecycle and proof-of-delivery
-- capture.  On departure each printer is moved to DISPATCHED; on
-- delivery each printer is moved to INSTALLED.  Cancellation from
-- IN_TRANSIT reverts DISPATCHED printers back to ALLOCATED.
--
-- Business rules enforced here / by the API:
--   BR-020  A printer must be in ALLOCATED status to be added to a
--           dispatch order (blocks dispatching unallocated printers).
--   BR-024  A dispatch order must have at least one printer.
--   BR-025  A printer may only appear on one active dispatch order at a
--           time (PENDING / SCHEDULED / IN_TRANSIT).
--
-- Lifecycle:
--   PENDING → SCHEDULED → IN_TRANSIT → DELIVERED  (terminal)
--   PENDING | SCHEDULED | IN_TRANSIT → CANCELLED  (terminal)
-- =====================================================================

