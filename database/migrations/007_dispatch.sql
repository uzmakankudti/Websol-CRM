-- ---------------------------------------------------------------------
-- Idempotent cleanup — drop in child → parent order so a partial previous
-- run does not leave orphaned tables or mismatched FK types behind.
-- Safe to run on a fresh database (IF EXISTS is a no-op then).
-- ---------------------------------------------------------------------
DROP TABLE IF EXISTS dispatch_order_items;
DROP TABLE IF EXISTS dispatch_orders;

-- ---------------------------------------------------------------------
-- dispatch_orders — one header per delivery run.
-- order_no is generated by the API: DSP-YYYY-NNNN.
--
-- FK type audit (must match referenced column exactly):
--   contract_id  BIGINT UNSIGNED  → contracts.id        BIGINT UNSIGNED  ✓
--   site_id      INT UNSIGNED     → customer_sites.id   INT UNSIGNED     ✓
--   created_by   BIGINT UNSIGNED  → users.id            BIGINT UNSIGNED  ✓
-- ---------------------------------------------------------------------
CREATE TABLE dispatch_orders (
  id            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  order_no      VARCHAR(40)     NOT NULL,
  contract_id   BIGINT UNSIGNED NOT NULL,
  site_id       INT UNSIGNED    NULL,
  status        ENUM('PENDING','SCHEDULED','IN_TRANSIT','DELIVERED','CANCELLED')
                NOT NULL DEFAULT 'PENDING',
  planned_date  DATE            NULL,
  courier       VARCHAR(200)    NULL,
  tracking_ref  VARCHAR(100)    NULL,
  departed_at   DATETIME        NULL,
  delivered_at  DATETIME        NULL,
  pod_recipient VARCHAR(200)    NULL,
  pod_notes     TEXT            NULL,
  notes         TEXT            NULL,
  created_by    BIGINT UNSIGNED NOT NULL,
  created_at    DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at    DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_dispatch_order_no (order_no),
  CONSTRAINT fk_dsp_contract FOREIGN KEY (contract_id) REFERENCES contracts     (id),
  CONSTRAINT fk_dsp_site     FOREIGN KEY (site_id)     REFERENCES customer_sites (id) ON DELETE SET NULL,
  CONSTRAINT fk_dsp_creator  FOREIGN KEY (created_by)  REFERENCES users          (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ---------------------------------------------------------------------
-- dispatch_order_items — one row per printer on the order.
-- BR-020: printer_id must reference a printer whose status is ALLOCATED
-- at the time the order is created (enforced by the API).
-- BR-025: uniqueness of active dispatch per printer is enforced by API
-- query before insert.
--
-- FK type audit:
--   dispatch_order_id  BIGINT UNSIGNED  → dispatch_orders.id  BIGINT UNSIGNED  ✓
--   printer_id         BIGINT UNSIGNED  → printers.id         BIGINT UNSIGNED  ✓
-- ---------------------------------------------------------------------
CREATE TABLE dispatch_order_items (
  id                BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  dispatch_order_id BIGINT UNSIGNED NOT NULL,
  printer_id        BIGINT UNSIGNED NOT NULL,
  notes             TEXT            NULL,
  CONSTRAINT fk_dsp_item_order   FOREIGN KEY (dispatch_order_id) REFERENCES dispatch_orders (id) ON DELETE CASCADE,
  CONSTRAINT fk_dsp_item_printer FOREIGN KEY (printer_id)        REFERENCES printers         (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

